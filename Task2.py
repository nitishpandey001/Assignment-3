import math
num=int(input("Enter a number: "))
d=math.sqrt(num)
print(f"The square root of {num} is {d}")
e=math.log(num)
print(f"The logarithm of {num} is {e}")
f=math.radians(num)   #suppose that give data  in degree so converting into radian
g=math.sin(f)
print(f"The sine of {num} is {g}")
